package com.collection;

final class ImmutableClass {
	private final int id = 101;
	private final String name = "vivek";
	private final double salary = 25000.0;
	
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public double getSalary() {
		return salary;
	}

}

